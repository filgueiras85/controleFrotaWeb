    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        PrintWriter out = response.getWriter();
        request.setCharacterEncoding("UTF-8");
        response.setContentType("text/html");
        HttpSession sessao = request.getSession();
        String usuario = (String) sessao.getAttribute("jUsuario");
        if (usuario == null) {
            out.print("Nenhum usu√°rio autenticado!<br/>");
            out.print("<a href='../login.jsp'>Voltar para login</a>");
        } else {
            response.sendRedirect("alteracaoveiculo.jsp");
        }
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        request.setCharacterEncoding("UTF-8");
        VeiculoController veiculoController = new VeiculoController();
        Veiculo veiculo = veiculoController.listaUnicoVeiculo(request.getParameter("placa"));
        request.setAttribute("veiculo", veiculo);
        RequestDispatcher rd = request.getRequestDispatcher("/admin/alteracaodadosveiculo.jsp");
        rd.forward(request, response);
    }

